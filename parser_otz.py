from bs4 import BeautifulSoup
import requests
import time

def get_html(url):
    file_news = []
    headers = {
        'User-Agent': 'Mozilla/5.0 (Macintosh; Intel Mac OS X 10.9; rv:45.0) Gecko/20100101 Firefox/45.0'
    }
    r = requests.get('https://www.wildberries.ru/catalog/'+url+'/detail.aspx', headers=headers)  # Получаем метод Response
    r.encoding = 'utf8'  # У меня были проблемы с кодировкой, я задал в ручную
    soup = BeautifulSoup(r.text, 'html.parser')
    news_link = soup.find_all('i', {'data-link': "{include tmpl='productCardCommentsCount'}"})
    news_link = str(news_link)
    news_link = news_link[news_link.find(">")+1:news_link.rfind(">")-3]
    news_link = news_link.strip()

    return news_link



